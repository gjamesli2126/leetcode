from typing import List
from pprint import pprint as pp
from collections import deque
class Solution:
    def numIslands(self, grid: List[list]) -> int:
        xMax,yMax=len(grid[0]),len(grid)
        stack=deque([])
        island=0
        for y in range(yMax):
            for x in range(xMax):


                if grid[y][x]=="0":
                    continue
                stack.append((x,y))
                while stack:
                    px,py=stack.popleft()
                    if grid[py][px]=="0":
                        continue

                    grid[py][px] = "0"
                    for dx,dy in [(0,1),(1,0),(-1,0),(0,-1)]:
                        if dx+px<0 or dx+px>=xMax or dy+py<0 or dy+py>=yMax:
                            continue
                        stack.append((px+dx,py+dy))
                island+=1
        return island





